// 配列を定義
const arr = ["r", "i", "c", "o", "h"];

// インデックス3（"o"）の要素を1つ削除
arr.splice(3, 1);

// 削除後の配列の中身を確認
console.log(arr); // [ 'r', 'i', 'c', 'h' ]

// 配列の長さを確認
console.log(arr.length); // 4

/*
【解説】

1. arr.splice(3, 1) の意味：
   → インデックス3の位置から、1つの要素を削除する

2. 配列の中身：
   → "o" が削除されて、残りは [ 'r', 'i', 'c', 'h' ]

3. 配列の長さ：
   → 要素を1つ削除したので、長さは5 → 4 に減る

【補足】
・spliceは配列の中身をしっかり詰め直してくれる
・deleteのように「空のまま残る」ことがないので、通常はこちらを使う方が安全で自然
*/
